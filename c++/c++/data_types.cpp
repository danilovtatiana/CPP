#include <iostream>
using namespace std;

int main()
{
    //numbers
    int myNum = 10; //integers
    float myN = 5.75; //float
    double myD = 19.99; //double
    
    //boolean
    
    bool isCodingFun = true;
    bool isFishTasty = false;
    
    //character data types
    
    char myGrade = 'B';
    
    //ASCII values
    char a =65, b = 66, c = 67;
    
    //strings
    
    string greeting = "Hello";
    
    cout << myNum <<"\n";
    cout << myN <<"\n";
    cout << myD <<"\n";
    
    cout << isCodingFun <<"\n";
    cout << isFishTasty <<"\n";
    
    cout <<myGrade <<"\n";
    
    cout << a <<"\n";
    cout << b <<"\n";
    cout << c <<"\n";
    
    cout << greeting <<"\n";
    
    return 0;
}
